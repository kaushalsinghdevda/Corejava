package in.com.oop;

public class Person {
	
	private int id;
	private String firstName;
	private String lastName;
	
	
	
	
	public void setId(int id) {
		this.id = id;
		
		
	}
	public int getId() {
		return id;
	}
	
	
	public void setfirstName(String n) {
		this.firstName = n;
		
		
	}
	public String  getfirstname() {
		return firstName;
	}
	
	public void setlastName(String k) {
		this.lastName = k;
		
		
	}
	public String getlastName() {
		return lastName;
	}
	
	
	

}
